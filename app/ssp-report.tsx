function fmtTarget(v: number, unit: SspTarget["unit"]) {
  if (unit === "₽") return `${fmtMln(v)} млн ₽`;
  if (unit === "%") return `${v.toLocaleString("ru-RU", { maximumFractionDigits: 2 })}%`;
  return `${v} ${unit}`;
}

