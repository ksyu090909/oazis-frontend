// Разворачивает задачи в плоский список статусов (пункт без подзадач + все подзадачи)
function cooStatuses(m: CooMonth): Status[] {
  return m.plan.flatMap(p => (p.sub ? p.sub.map(s => s.status) : p.status ? [p.status] : []));
}

