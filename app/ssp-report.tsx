/** Сгруппированная столбчатая диаграмма план/факт по месяцам (инлайн-SVG). */
function PlanFactChart({ data, unitHint, planLabel = "План", factLabel = "Факт" }: {
  data: { label: string; plan: number; fact: number }[]; unitHint?: string; planLabel?: string; factLabel?: string;
}) {
  const W = 720, H = 210, padL = 8, padR = 8, padT = 16, padB = 34;
  const chartW = W - padL - padR, chartH = H - padT - padB;
  const max = Math.max(...data.flatMap(d => [d.plan, d.fact]), 1) * 1.12;
  const groupW = chartW / data.length;
  const barW = Math.min(26, groupW * 0.32);
  const gap = 4;
  return (
    <div>
      <div style={{ display: "flex", gap: 16, alignItems: "center", marginBottom: 6, flexWrap: "wrap" }}>
        <span style={{ display: "inline-flex", alignItems: "center", gap: 6, fontSize: 12, color: "var(--ink-2)" }}>
          <span style={{ width: 11, height: 11, borderRadius: 3, background: "var(--faint)", display: "inline-block" }} aria-hidden />{planLabel}
        </span>
        <span style={{ display: "inline-flex", alignItems: "center", gap: 6, fontSize: 12, color: "var(--ink-2)" }}>
          <span style={{ width: 11, height: 11, borderRadius: 3, background: "var(--brand)", display: "inline-block" }} aria-hidden />{factLabel}
        </span>
        {unitHint && <span style={{ fontSize: 12, color: "var(--muted)" }}>{unitHint}</span>}
      </div>
      <div style={{ overflowX: "auto" }}>
        <svg viewBox={`0 0 ${W} ${H}`} style={{ width: "100%", minWidth: 460, height: "auto", display: "block" }} role="img">
          {[0, 0.5, 1].map(t => (
            <line key={t} x1={padL} x2={W - padR} y1={padT + chartH * (1 - t)} y2={padT + chartH * (1 - t)}
              stroke="var(--border)" strokeWidth={1} />
          ))}
          {data.map((d, i) => {
            const x0 = padL + groupW * i + groupW / 2;
            const hPlan = (d.plan / max) * chartH;
            const hFact = (d.fact / max) * chartH;
            const pct = d.plan > 0 ? Math.round((d.fact / d.plan) * 100) : 0;
            return (
              <g key={i}>
                <rect x={x0 - barW - gap / 2} y={padT + chartH - hPlan} width={barW} height={Math.max(1, hPlan)}
                  rx={3} fill="var(--faint)" opacity={0.55} />
                <rect x={x0 + gap / 2} y={padT + chartH - hFact} width={barW} height={Math.max(1, hFact)}
                  rx={3} fill={scoreColor(Math.min(pct, 100))} opacity={0.9} />
                <text x={x0} y={H - 16} textAnchor="middle" fontSize={12} fill="var(--muted)">{d.label}</text>
                <text x={x0} y={H - 3} textAnchor="middle" fontSize={11} fontWeight={600} fill={scoreInk(Math.min(pct, 100))}>{pct}%</text>
              </g>
            );
          })}
        </svg>
      </div>
    </div>
  );
}

/** Полоса статусов задач: сколько выполнено / в процессе / не выполнено. */
